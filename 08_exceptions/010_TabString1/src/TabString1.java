public class TabString1
{
    public static void main(String argv[])
    {
        int i;
        String tab[] = new String[3];
        tab[0] = "Autruche";
        tab[1] = "Bruant";
        tab[2] = "Corbeau";

        for (i = 0; i <= 4; i++)
        {
            System.out.println(tab[i]);
        }
    }
}
